    private void btnXoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXoaActionPerformed
        try {
            if (txtMaKH.getText().trim().isBlank()) {
                JOptionPane.showMessageDialog(this, "Vui lòng nhập mã muốn xóa");
            } else {
                int xoa = JOptionPane.showConfirmDialog(this, "Bnaj có chắc muốn xóa khônng", "Hỏi", JOptionPane.YES_NO_OPTION);
                if (xoa == JOptionPane.YES_OPTION) {
                    _KhachHangISevice.DeleteKhachHangById(txtMaKH.getText());
                    JOptionPane.showMessageDialog(this, "Xóa thành công");
                    Loadata();
                    reset();
                }
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Lỗi kiểu dữ liệu");
        }
    }//GEN-LAST:event_btnXoaActionPerformed
    private void reset() {
        txtMaKH.setText("");
        txtHo.setText("");
        txtTen.setText("");
        txtSDT.setText("");
        txtDiaChi.setText("");
        buttonGroup1.clearSelection();

    }

    private void btnThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemActionPerformed
        try {
            if (vali()) {
                int trangthai;
                if (rdoHoatDong.isSelected()) {
                    trangthai = 10;
                } else {
                    trangthai = 0;
                }
                KhachHangVM kh = new KhachHangVM();
                kh.setMaKH(txtMaKH.getText());
                kh.setHoKH(txtHo.getText());
                kh.setTenKH(txtTen.getText());
                kh.setSDT(txtSDT.getText());
                kh.setDiaChi(txtDiaChi.getText());
                kh.setTrangThai(trangthai);
                if (_KhachHangISevice.createNewKhachHang(kh)) {
                    JOptionPane.showMessageDialog(this, "Thêm thành công");
                    Loadata();
                } else {
                    JOptionPane.showMessageDialog(this, "Thêm thất bại");
                }
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Lỗi kiểu dữ liệu");
        }
    }//GEN-LAST:event_btnThemActionPerformed

    private void btnTimActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTimActionPerformed

        if (txtTim.getText().trim().isBlank()) {
            JOptionPane.showMessageDialog(this, "Vui lòng nhập mã muốn tìm");
        } else {
            model.setRowCount(0);
            ds = _KhachHangISevice.getKhachHangByID(txtTim.getText());
            model = (DefaultTableModel) tblKh.getModel();
            JOptionPane.showMessageDialog(this, "Tìm thành công");
            LoadDataTaBle();
        }
    }//GEN-LAST:event_btnTimActionPerformed

