    private void LoadDataTaBle() {
        String trangThai = null;
        for (KhachHangVM kh : ds) {
            if (kh.getTrangThai() == 10) {
                trangThai = "Còn hoạt động";
            } else {
                trangThai = "Không hoạt động";
            }
            model.addRow(new String[]{kh.getMaKH(), kh.getHoKH(), kh.getTenKH(), kh.getSDT(), kh.getDiaChi(), trangThai});
        }
        tblKh.setModel(model);
    }

    private void Loadata() {
        model.setRowCount(0);
        ds = _KhachHangISevice.getAll();
        model = (DefaultTableModel) tblKh.getModel();
        LoadDataTaBle();
    }

    private boolean vali() {
        String sdt= "^(0|84)(2(0[3-9]|1[0-6|8|9]|2[0-2|5-9]|3[2-9]|4[0-9]|5[1|2|4-9]|6[0-3|9]|7[0-7]|8[0-9]|9[0-4|6|7|9])|3[2-9]|5[5|6|8|9]|7[0|6-9]|8[0-6|8|9]|9[0-4|6-9])([0-9]{7})$";
        if (txtMaKH.getText().trim().isBlank()) {
            JOptionPane.showMessageDialog(this, "Mã không được để trống");
            return false;
        } else if (txtHo.getText().trim().isBlank()) {
            JOptionPane.showMessageDialog(this, "Họ không được để trống");
            return false;
        } else if (txtTen.getText().trim().isBlank()) {
            JOptionPane.showMessageDialog(this, "Tên không được để trống");
            return false;
        } else if (txtSDT.getText().trim().isBlank()) {
            JOptionPane.showMessageDialog(this, "SDt không được để trống");
            return false;
        }else if(!txtSDT.getText().matches(sdt)){
             JOptionPane.showMessageDialog(this, "SDt không đúng định dạng(03,09)");
            return false;
        }
        else if (txtDiaChi.getText().trim().isBlank()) {
            JOptionPane.showMessageDialog(this, "Địa chỉ không được để trống");
            return false;
        } else if (buttonGroup1.isSelected(null)) {
            JOptionPane.showMessageDialog(this, "Trạng thái không được để trống");
            return false;
        }
        return true;
    }

