    private void btnSuaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSuaActionPerformed
        int row = tblSanPham.getSelectedRow();
        if (row == -1) {
            JOptionPane.showMessageDialog(this, "Chưa chọn dòng cần sửa!");
            return;
        }
        SanPham sanPhamSua = getFormData();
        String maSPSua = txtMaSP.getText();
        int confirm = JOptionPane.showConfirmDialog(this, "Bạn có muốn sửa không?");
        if (confirm == JOptionPane.YES_OPTION) {
            qLSP.suaSanPham(sanPhamSua, maSPSua);
            JOptionPane.showMessageDialog(this, "Sửa thành công!");
            clearFormSP();
            loadTableSanPham(qLSP.getListSanPhamDB());
        }
    }//GEN-LAST:event_btnSuaActionPerformed

