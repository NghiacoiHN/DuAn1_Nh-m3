    private void loadTableNhanVien(ArrayList<NhanVien> listNSXDB){
        listNV = listNSXDB;
        dTMNhanVien = (DefaultTableModel) tblNhanVien.getModel();
        dTMNhanVien.setRowCount(0);
        int sTT = 1;
        for (NhanVien nhanVien : listNSXDB) {
            for (CuaHang cuaHang : qLCH.getListCuaHangDB()) {
                if (nhanVien.getCuaHang().getiD() != null && cuaHang.getiD() != null) {
                    if (nhanVien.getCuaHang().getiD().equals(cuaHang.getiD())) {
                        nhanVien.setCuaHang(cuaHang);
                    }
                }
            }
            for (ChucVu chucVu : qLCV.getListChucVuDB()) {
                if (nhanVien.getChucVu().getiD() != null && chucVu.getiD() != null) {
                    if (nhanVien.getChucVu().getiD().equals(chucVu.getiD())) {
                        nhanVien.setChucVu(chucVu);
                    }
                }
            }
            dTMNhanVien.addRow(new Object[]{
                sTT++,
                nhanVien.getMa(),
                nhanVien.getTen(),
                nhanVien.getTenDem(),
                nhanVien.getHo(),
                nhanVien.getGioiTinh(),
                nhanVien.getNgaySinh(),
                nhanVien.getDiaChi(),
                nhanVien.getsDT(),
                nhanVien.getMatKhau(),
                nhanVien.getCuaHang().toString(),
                nhanVien.getChucVu().toString(),
                nhanVien.getiDGuiBC(),
                nhanVien.getTrangThai() == 0 ? "Còn làm việc" : "Nghỉ việc"
            });
        }
    }

