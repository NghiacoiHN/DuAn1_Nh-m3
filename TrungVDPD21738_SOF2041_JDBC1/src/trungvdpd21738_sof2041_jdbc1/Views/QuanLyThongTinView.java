    private void tblNhanVienMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblNhanVienMouseClicked
        int row = tblNhanVien.getSelectedRow();
        NhanVien nhanVien = listNV.get(row);
        for (CuaHang cuaHang : listCH) {
            if (nhanVien.getCuaHang().getiD() != null && cuaHang.getiD() != null) {
                if (nhanVien.getCuaHang().getiD().equals(cuaHang.getiD())) {
                    nhanVien.setCuaHang(cuaHang);
                }
            }
        }
        
        for (ChucVu chucVu : listCV) {
            if (nhanVien.getChucVu().getiD() != null && chucVu.getiD() != null) {
                if (nhanVien.getChucVu().getiD().equals(chucVu.getiD())) {
                    nhanVien.setChucVu(chucVu);
                }
            }
        }
        
        txtMaNV.setText(tblNhanVien.getValueAt(row, 1).toString());
        txtTenNV.setText(tblNhanVien.getValueAt(row, 2).toString());
        txtTenDemNV.setText(tblNhanVien.getValueAt(row, 3).toString());
        txtHoNV.setText(tblNhanVien.getValueAt(row, 4).toString());
        rdoNamNV.setSelected(tblNhanVien.getValueAt(row, 5).equals("Nam") ? true : false);
        rdoNuNV.setSelected(tblNhanVien.getValueAt(row, 5).equals("Nữ") ? true : false);
        txtNgaySinhNV.setText(tblNhanVien.getValueAt(row, 6).toString());
        txtDiaChiNV.setText(tblNhanVien.getValueAt(row, 7).toString());
        txtSDT.setText(tblNhanVien.getValueAt(row, 8).toString());
        txtMatKhauNV.setText(tblNhanVien.getValueAt(row, 9).toString());
        cbbCuaHangNV.setSelectedItem(nhanVien.getCuaHang());
        cbbChucVuNV.setSelectedItem(nhanVien.getChucVu());
//        txtIDGuiBC.setText(tblNhanVien.getValueAt(row, 12).toString());
        chkTrangThaiNV.setSelected(tblNhanVien.getValueAt(row, 13).equals("Còn làm việc") ? true : false);
    }//GEN-LAST:event_tblNhanVienMouseClicked

