    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        SanPhamTest sanPhamTest = getFormData();
        int confirm = JOptionPane.showConfirmDialog(this, "Bạn có muốn thêm sản phẩm mới không?");
        if(confirm == JOptionPane.YES_OPTION){
            JOptionPane.showMessageDialog(this, qLSP.themSanPham(sanPhamTest));
            clearForm();
            loadData(qLSP.getListSPTest());
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        SanPhamTest sanPhamTest = getFormData();
        int row = tblSanPham.getSelectedRow();
        String iDCTSP = listSP.get(row).getId();
        
        System.out.println(iDCTSP);
        int confirm = JOptionPane.showConfirmDialog(this, "Bạn có muốn sửa sản phẩm mới không?");
        if(confirm == JOptionPane.YES_OPTION){
            JOptionPane.showMessageDialog(this, qLSP.updateSPTest(sanPhamTest, iDCTSP));
            clearForm();
            loadData(qLSP.getListSPTest());
        }
    }//GEN-LAST:event_jButton2ActionPerformed

