    private void loadData(ArrayList<SanPhamTest> listSPTest) {
        dTM = (DefaultTableModel) tblSanPham.getModel();
        dTM.setRowCount(0);
        for (SanPhamTest sanPhamTest : listSPTest) {
            dTM.addRow(new Object[]{
                sanPhamTest.getId(),
                sanPhamTest.getNamBH(),
                sanPhamTest.getMoTa(),
                sanPhamTest.getSoLuongTon(),
                sanPhamTest.getGiaNhap(),
                sanPhamTest.getSoLuongTon() >= 900 ? "Nhiều sản phẩm":"Ít sản phẩm"
            });
        }
    }

    private void loadCBB() {
        dcbm = (DefaultComboBoxModel) cbbNamBH.getModel();
        ArrayList<String> namBH = new ArrayList<>();
        namBH.add("1990");
        namBH.add("1991");
        namBH.add("1992");
        namBH.add("1993");
        namBH.add("1994");
        namBH.add("1995");
        namBH.add("1996");
        namBH.add("1997");
        namBH.add("1998");
        namBH.add("1999");
        namBH.add("2000");
        for (String string : namBH) {
            dcbm.addElement(string);
        }
    }

    private SanPhamTest getFormData() {
        return new SanPhamTest(Integer.parseInt(txtNamBH.getText()),txaMoTa.getText() , Integer.parseInt(txtSoLuongTon.getText()), Double.parseDouble(txtGiaNhap.getText()));
    }

    private void clearForm() {
        txtGiaNhap.setText("");
        txtNamBH.setText("");
        txtSoLuongTon.setText("");
        txaMoTa.setText("");
        cbbNamBH.setSelectedItem(1);
    }
